    void listarVentas()
    {
        /**
         * generar el reporte de ventas, obtener las ventas y mostrarlas en la tabla
         */
        
        List<ReporteVentas> lista = ventasdao.obtenerVentas();
        tablaReporte = (DefaultTableModel)tblReporteGral.getModel();
        Object[] obj = new Object[8];
        for (int i = 0; i < lista.size(); i++)
        {
            obj[0] = lista.get(i).getIdVentas();
            obj[1] = lista.get(i).getIdCliente();
            obj[2] = lista.get(i).getDni();
            obj[3] = lista.get(i).getNombreCliente();
            obj[4] = lista.get(i).getDirCliente();
            obj[5] = lista.get(i).getClaveVenta();
            obj[6] = lista.get(i).getFecha();
            obj[7] = lista.get(i).getMonto();
            tablaReporte.addRow(obj);
        }
        tblReporteGral.setModel(tablaReporte);
    }

