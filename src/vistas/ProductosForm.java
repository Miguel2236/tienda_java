    void listar()
    {
        /**
         * Listar los productos en la tabla
         */
        List<Producto> lista = dao.listar();
        model = (DefaultTableModel)tblProducto.getModel();
        Object[]obj = new Object[5];
        for (int i = 0; i < lista.size(); i++)
        {
            obj[0] = lista.get(i).getId();
            obj[1] = lista.get(i).getNom();
            obj[2] = lista.get(i).getPrecio();
            obj[3] = lista.get(i).getStock();
            obj[4] = lista.get(i).getEstado();
            model.addRow(obj);
        }
        tblProducto.setModel(model);
    }
    
    void agregarProducto()
    {
        /**
         * agregar un produto
         */
        
        String nombre = txtNombre.getText();
        String precio = txtPrecio.getText();
        String stock = txtStock.getText();
        String es = cmbEstado.getSelectedItem().toString();
        Object[] obj = new Object[4];
        obj[0] = nombre;
        obj[1] = precio;
        obj[2] = stock;
        obj[3] = es;
        
        dao.add(obj); // llamar al dao para invocar la funcion de guardado enviando los datos
    }
    
    void limpiarTabla()
    {
        /**
         * funciÃ³n para limpiar los inputs y la tabla
         */
        for (int i = 0; i < model.getRowCount(); i++) 
        {
            // limpiar tabla
            model.removeRow(i);
            i = i - 1;
        }
        
        // limpiar inputs
        txtNombre.setText("");
        txtPrecio.setText("");
        txtStock.setText("");
        cmbEstado.setSelectedItem("- Seleccionar -");
    }

